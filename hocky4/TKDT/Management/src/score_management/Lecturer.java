package score_management;

import java.util.Date;

public class Lecturer {
	private String name;
	private Date dateOfBirth;
	private String degree;

	public Lecturer(String name, Date dateOfBirth, String degree) {
		super();
		this.name = name;
		this.dateOfBirth = dateOfBirth;
		this.degree = degree;
	}

}
